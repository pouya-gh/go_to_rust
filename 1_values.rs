fn main() {
  println!("{}", "a string".to_string()); // String
  println!("this is an str"); // str
  println!("{}", 3.14 * 6.45); // float (f64)
  // rust's numeric types:
  // i8, i16, i32, i64 (signed integer)
  // u8, u16, u32, u64 (unsigned integer)
  // isize, usize
  // f32, f64 (float)
  println!("{}", true || true); // bool
}
